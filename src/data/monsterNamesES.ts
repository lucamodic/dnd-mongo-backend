const pluralize = (word: string) => {
  if (word.endsWith("z")) return `${word.slice(0, -1)}ces`;
  if (/[aeiouáéíóú]$/i.test(word)) return `${word}s`;
  return `${word}es`;
};

const titleCase = (text: string) =>
  text
    .split(" ")
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");

const translateWords = (name: string) =>
  titleCase(
    name
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, " ")
      .split(" ")
      .filter(Boolean)
      .map((word) => WORDS[word] || word)
      .join(" ")
  );

export function monsterNameES(index: string, fallbackName: string): string {
  if (MONSTER_NAMES_ES[index]) return MONSTER_NAMES_ES[index];

  const parts = index.split("-");
  const age = DRAGON_AGES[parts[0]];
  const color = COLORS[parts[1]];
  if (age && color && parts[2] === "dragon") {
    return `Dragón ${color} ${age}`;
  }

  if (index.startsWith("swarm-of-")) {
    const translated = translateWords(index.replace("swarm-of-", "").replace(/-/g, " "));
    return `Enjambre de ${pluralize(translated)}`;
  }

  if (index.startsWith("giant-")) {
    const translated = translateWords(index.replace("giant-", "").replace(/-/g, " "));
    return `${translated} Gigante`;
  }

  return translateWords(fallbackName || index);
}
